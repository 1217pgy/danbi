package game;

public class Character {
    protected int no;
    protected String name;
    protected String image;
    protected int hp;
    protected int maxHp;
    protected int mp;
    protected int maxMp;
    protected int level;
    protected int exp;
    protected int gold;
    protected int map;
    protected int seed;
    protected int x;
    protected int y;
    protected int direction;
    protected int moveSpeed;
    protected int team;

    public int getNo() {
        return no;
    }

    public String getName() {
        return name;
    }

    public String getImage() {
        return image;
    }

    public int getHp() {
        return hp;
    }

    public int getMaxHp() {
        return maxHp;
    }

    public int getMp() {
        return mp;
    }

    public int getMaxMp() {
        return maxMp;
    }

    public int getLevel() {
        return level;
    }

    public int getExp() {
        return exp;
    }

    public int getGold() {
        return gold;
    }

    public int getMap() {
        return map;
    }

    public int getSeed() {
        return seed;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getDirection() {
        return direction;
    }

    public int getMoveSpeed() {
        return moveSpeed;
    }

    public int getTeam() {
        return team;
    }
}
